a= 10
b= 2
calc = input("Enter + or - or * or /: ")
if(calc == "+"):
    print("Addition of a and b is",a+b)
elif(calc == "-"):
    print("Subraction of a and b is",a-b)
elif(calc == "*"):
    print("Multiplication of a and b is",a*b)
elif(calc == "/"):
    print("Division of a and b is",a/b)
else:
    print("Invalid data")
